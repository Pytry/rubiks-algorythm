package org.xitikit.rubiks.rubiksalgorythm.actions;

/**
 * Created by Keith on 9/4/2017.
 */
public enum ActionCode
{
  /**
   * Indicates that the FRONT of the cube should be turned clockwise.
   */
  F,
  /**
   * Indicates that the FRONT of the cube should be turned counter-clockwise.
   */
  FC,
  /**
   * Indicates that the BACK of the cube should be turned clockwise.
   */
  BK,
  /**
   * Indicates that the BACK of the cube should be turned counter clockwise.
   */
  BKC,
  /**
   * Indicates that the LEFT of the cube should be turned clockwise.
   */
  L,
  /**
   * Indicates that the LEFT of the cube should be turned counter-clockwise.
   */
  LC,
  /**
   * Indicates that the RIGHT of the cube should be turned clockwise.
   */
  R,
  /**
   * Indicates that the RIGHT of the cube should be turned counter-clockwise.
   */
  RC,
  /**
   * Indicates that the TOP of the cube should be turned clockwise.
   */
  T,
  /**
   * Indicates that the TOP of the cube should be turned counter-clockwise.
   */
  TC,
  /**
   * Indicates that the BOTTOM of the cube should be turned clockwise.
   */
  B,
  /**
   * Indicates that the BOTTOM of the cube should be turned counter-clockwise.
   */
  BC
}
